class Song {
    constructor(title, artist, album, cover, duration, preview, albumId, artistId) {
        this.title = title;
        this.artist = artist;
        this.album = album;
        this.cover = cover;
        this.duration = duration;
        this.preview = preview;
        this.albumId = albumId;
        this.artistId = artistId;
    }
}

export {
    Song
}